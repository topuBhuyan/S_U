int main()
{
	int n ;
	while (1)
		{
			cout<<"Enter a number = " ;
		cin >> n ;
		cout << fib(n) <<endl ;
		}
		return 0 ;
}
int fib(int n)
{
	if (n < 2) return n ;
	else return fib(n-1) + fib (n-2) ;
}
